package com.zikezhi.entity;

import java.util.Date;

public class BiaoQian {

	private int b_id;
	
	private String b_content;
	
	private Date b_time;
	
	private String b_uid;
	
	private String b_img;
	
	private String b_eid;

	public int getB_id() {
		return b_id;
	}

	public void setB_id(int b_id) {
		this.b_id = b_id;
	}

	public String getB_content() {
		return b_content;
	}

	public void setB_content(String b_content) {
		this.b_content = b_content;
	}

	public Date getB_time() {
		return b_time;
	}

	public void setB_time(Date b_time) {
		this.b_time = b_time;
	}

	public String getB_uid() {
		return b_uid;
	}

	public void setB_uid(String b_uid) {
		this.b_uid = b_uid;
	}

	public String getB_img() {
		return b_img;
	}

	public void setB_img(String b_img) {
		this.b_img = b_img;
	}

	public String getB_eid() {
		return b_eid;
	}

	public void setB_eid(String b_eid) {
		this.b_eid = b_eid;
	}
}
